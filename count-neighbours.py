def count_neighbours(grid, row, col):
    nrows = [nr for nr in range(row-1, row+2) if 0 <= nr < len(grid)]
    ncols = [nc for nc in range(col-1, col+2) if 0 <= nc < len(grid[0])]
    neighbours = [(nrow, ncol) for nrow in nrows for ncol in ncols]
    neighbours.remove((row, col))
    return sum([grid[nr][nc] for nr, nc in neighbours])

